def soma(num1, num2):
    num1 = num1
    num2 = num2
    soma_total = num1 + num2
    return soma_total


def sub(num1, num2):
    num1 = num1
    num2 = num2
    sub_total = num1 - num2
    return sub_total


def mult(num1, num2):
    num1 = num1
    num2 = num2
    mult_total = num1 * num2
    return mult_total


def div(num1, num2):
    num1 = num1
    num2 = num2
    div_total = num1 / num2
    return div_total
